						Como primera fortaleza de nuestra institución debemos mencionar a los estudiantes. Los mismos son seleccionados bajo un riguroso proceso, el cual permite asegurarnos que se cuenta con el recurso humano al cual se le deberá formar en valores y habilidades de aprendizaje. A lo largo de estos casi 9 años de experiencia institucional, nuestros alumnos han demostrado estar a la altura de las exigencias que se plantean para su desarrollo integral en el plano nacional e internacional.
	
						Es de suma importancia mencionar como segunda fortaleza a nuestros docentes, los cuales provienen de diferentes contextos educativos, por lo cual enriquecen cada uno de los documentos de gestión y técnico-pedagógicos. Así, el trabajo refleja una experticia en el manejo de conceptos, desempeños, competencias, indicadores que van acorde a las exigencias de la Red COAR y del Bachillerato Internacional, en su Programa del Diploma.
	
						Como tercera fortaleza, la Dirección de Bienestar y Desarrollo del Estudiante se encarga de facilitar a nuestros estudiantes el complemento a sus aprendizajes basados en el acompañamiento socioemocional, nutricional y de salud. Así, desde cada una de las coordinaciones se desarrollan las estrategias más adecuadas para tener como resultado personas integras que conviven de forma pacífica.
	
