						Durante los primeros años, el Colegio Mayor se fue posicionando en la sociedad peruana por su calidad educativa. En el año 2010, obtuvo tras un arduo trabajo, la autorización para impartir el Programa del Diploma del Bachillerato Internacional. En el año 2015, se convirtió en el primer colegio público en acreditarse por el Sistema Nacional de Evaluación, Acreditación y Certificación de la Calidad Educativa.
	
